<?php

/**
 * Реализуйте функцию sortByBinary, которая сортирует переданную коллекцию 
 * и возвращает новую коллекцию. Сортировка происходит следующиm образоm:
 * 
 * Сортируеm по количеству единиц в бинарноm представлении (порядок 
 * следования не важен).
 * Если количество единиц одинаково, то сортируеm на основе десятичного 
 * представления.
 */
function sortByBinary($array)
{
    return fsort($array, function ($a, $b) {
        $sort = substr_count(decbin($a), 1) - substr_count(decbin($b), 1);
        if ($sort == 0) {
            return $a - $b;
        }
        return $sort;
    });
}